class MathDemo
{
	public static void main(String[] args)
	{ 
		System.out.println("Max of -8 and -4 is "+Math.max(-8,-4));//finding the maximum value
		System.out.println("Min of -8 and -4 is "+Math.min(-8,-4));//finding the minimum value
		System.out.println("Abs of -18 is "+Math.abs(-18));//finding the absolute value
		System.out.println("Ceiling of 45.7 is "+Math.ceil(45.7));//finding the closest upper whole number
		System.out.println("Floor of 45.7 is "+Math.floor(45.7));//finding the closest below whole number

	}

}